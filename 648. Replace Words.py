class Solution(object):
    def replaceWords(self, dict, sentence):
        """
        :type dict: List[str]
        :type sentence: str
        :rtype: str
        """
        trie = {}
        for word in dict:
            node = trie
            for c in word:
                if c not in node:
                    node[c] = {}
                node = node[c]
            node['#'] = word

        result = []
        for word in sentence.split(' '):
            node = trie
            for c in word:
                if c in node:
                    node = node[c]
                    if '#' in node:
                        word = node['#']
                        break
                else:
                    break
            result.append(word) # do not forget the whole word is a root's prefix
        return ' '.join(result)



